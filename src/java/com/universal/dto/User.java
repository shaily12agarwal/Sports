package com.universal.dto;

/**
 *
 * @author rahul
 */
public class User {

  
    int id;
    String name;
    String email;
    String phone;
    String address;
    String gender;
    String username;
    String password;
    String city;
    int age;
    String imageNmae;

    public String getImageNmae() {
        return imageNmae;
    }

    public void setImageNmae(String imageNmae) {
        this.imageNmae = imageNmae;
    }
    

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }

    public String getPhone() {
        return phone;
    }

    public void setPhone(String phone) {
        this.phone = phone;
    }

    public String getAddress() {
        return address;
    }

    public void setAddress(String address) {
        this.address = address;
    }

    public String getGender() {
        return gender;
    }

    public void setGender(String gender) {
        this.gender = gender;
    }

    public String getUsername() {
        return username;
    }

    public void setUsername(String username) {
        this.username = username;
    }

    public String getPassword() {
        return password;
    }

    public void setPassword(String password) {
        this.password = password;
    }

    public String getCity() {
        return city;
    }

    public void setCity(String city) {
        this.city = city;
    }

    public int getAge() {
        return age;
    }

    public void setAge(int age) {
        this.age = age;
    }

    @Override
    public String toString() {
        return "User{" + "id=" + id + ", name=" + name + ", email=" + email + ", phone=" + phone + ", address=" + address + ", gender=" + gender + ", username=" + username + ", password=" + password + ", city=" + city + ", age=" + age + ", imageNmae=" + imageNmae + '}';
    }

    
    
    
    
}



















